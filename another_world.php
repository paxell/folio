<p class='pleft'>This was a thesis project, the aim of which was to create a browser-based point and click adventure game from the point of view of a refugee. It followed the main character's adventure as he flees his homeworld in search of a safer place to live. It was set on another world so as to remain detached from real world conflicts involving refugees. The otherworldly setting also allowed for greater opportunities when it came to designing distinctive characters and locations.</p>
